export function ReportNav() {
  const [activeSection, setActiveSection] = useState('overview');

  useEffect(() => {
    const observer = new IntersectionObserver(
      (entries) => {
        const visible = entries
          .filter((e) => e.isIntersecting)
          .sort((a, b) => {
            // Pick the one closest to top of viewport
            return a.boundingClientRect.top - b.boundingClientRect.top;
          });
        if (visible.length > 0) {
          setActiveSection(visible[0].target.id);
        }
      },
      { rootMargin: '-80px 0px -60% 0px', threshold: 0 }
    );

    NAV_ITEMS.forEach((item) => {
      const el = document.getElementById(item.id);
      if (el) observer.observe(el);
    });

    return () => observer.disconnect();
  }, []);

  const scrollTo = (id: string) => {
    const el = document.getElementById(id);
    if (el) {
      el.scrollIntoView({ behavior: 'smooth', block: 'start' });
    }
  };

  return (
    <nav className="hidden lg:block fixed left-0 top-1/2 -translate-y-1/2 z-30 pl-4">
      <div className="bg-card-white/90 backdrop-blur-md rounded-2xl shadow-lg border border-gray-200/60 p-2">
        <ul className="space-y-1">
          {NAV_ITEMS.map((item) => {
            const isActive = activeSection === item.id;
            return (
              <li key={item.id}>
                <button
                  onClick={() => scrollTo(item.id)}
                  className={`group flex items-center gap-3 w-full px-3 py-2.5 rounded-xl text-left transition-all duration-200 ${
                    isActive
                      ? 'bg-signify-teal/10 text-signify-teal'
                      : 'text-signify-gray hover:bg-gray-50 hover:text-signify-dark'
                  }`}
                  title={item.label}
                >
                  <span className={`flex-shrink-0 ${isActive ? 'text-signify-teal' : 'text-signify-gray group-hover:text-signify-dark'}`}>
                    {item.icon}
                  </span>
                  <span className={`text-sm font-medium whitespace-nowrap ${
                    isActive ? 'text-signify-teal' : ''
                  }`}>
                    {item.label}
                  </span>
                </button>
              </li>
            );
          })}
        </ul>
      </div>
    </nav>
  );
}
